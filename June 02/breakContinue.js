//break 
let i=0;
for(i=0;i<=10;i++){
    if(i===4){
        break;
    }
    console.log(i);
}
console.log("For Loop End.");

//continue
let j=0;
for(j=0;j<=10;j++){
    if(j===4){
        continue;//takes back to loop again...
    }
    console.log(j);
}
console.log("For Loop End.");