let loveBirds = new Array("Parikshit");
loveBirds.push("loves","mangoes");//adds elements at the end
console.log(loveBirds);
loveBirds.pop();//removes elements from end
loveBirds.shift();//removes elements from start
loveBirds.unshift("mangoes");//adds elements at start
loveBirds.push("Parikshit");//adds elements at end
console.log(loveBirds);

//Note : pop(),push() are faster than shift(),unshift()